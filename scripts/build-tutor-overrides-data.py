def main():
    qs = json.loads((ROOT / "data" / "question-db.json").read_text(encoding="utf-8"))
    qids = [q["questionId"] for q in qs]
    missing = [x for x in qids if x not in DATA]
    extra = [x for x in DATA if x not in qids]
    if missing:
        raise SystemExit(f"Missing: {missing}")
    if extra:
        raise SystemExit(f"Extra: {extra}")

    for q in qs:
        qid = q["questionId"]
        o = DATA[qid]
        correct = int(q["answer"])
        wrong = o["wrongAnswerAnalysis"]
        for i in range(1, len(q["choices"]) + 1):
            if i == correct:
                continue
            if str(i) not in wrong:
                raise SystemExit(f"{qid}: missing wrong key {i} (correct={correct})")

    OUT.write_text(json.dumps(DATA, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print(f"Wrote {OUT} ({len(DATA)} entries)")


if __name__ == "__main__":
    main()
